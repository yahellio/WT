<?php
/*
    Получение через POST-запрос двух произвольных наборов чисел и объединение их в один набор таким образом,
    чтобы в результирующем наборе оказались все числа из первого набора и такие числа из второго набора, которых нет в первом
    (например, если в первом наборе есть числа «2 2 5 3 7 2», а во втором «2 4 4 85», то в результирующем наборе будут числа «2 2 5 3 7 2 4 4 85»
    («2» из второго набора не попадает в итоговый набор, т.к. «2» уже есть в первом наборе).
*/
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Получаем данные из POST-запроса
    $input1 = isset($_POST['set1']) ? $_POST['set1'] : '';
    $input2 = isset($_POST['set2']) ? $_POST['set2'] : '';

    // Преобразуем строки с числами в массивы
    $set1 = preg_split('/\s+/', trim($input1));
    $set2 = preg_split('/\s+/', trim($input2));

    // Создаем результирующий массив, начинающийся с первого набора
    $result = $set1;

    // Для каждого числа во втором наборе проверяем, есть ли оно в первом
    foreach ($set2 as $number) {
        if (!in_array($number, $set1)) {
            $result[] = $number;
        }
    }

    // Преобразуем результат обратно в строку
    $output = implode(' ', $result);

    // Выводим результат
    echo "<h1>Результирующий набор чисел: " . htmlspecialchars($output) . "</h1>";

} else {
    // Если запрос не POST, выводим форму для ввода данных
    echo '
    <form method="POST">
        <label for="set1">Первый набор чисел (разделены пробелами):</label><br>
        <input type="text" name="set1" id="set1" required><br><br>
        
        <label for="set2">Второй набор чисел (разделены пробелами):</label><br>
        <input type="text" name="set2" id="set2" required><br><br>
        
        <input type="submit" value="Объединить наборы">
    </form>
    ';
}